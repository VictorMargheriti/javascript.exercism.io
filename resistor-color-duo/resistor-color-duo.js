export const decodedValue = (arr) => {
  const COLORS = [
    'black',
    'brown',
    'red',
    'orange',
    'yellow',
    'green',
    'blue',
    'violet',
    'grey',
    'white',
  ];
  let colorA = COLORS.indexOf(arr[0])
  let colorB = COLORS.indexOf(arr[1])

  return colorA * 10 + colorB
};

